int	ft_str_is_printable(char *str)
{
	int		i;
	int		is_print;

	i = 0;
	is_print = 1;
	while (str[i] != '\0')
	{
		if (str[i] < 32 || str[i] == 127)
			is_print = 0;
		i++;
	}
	return (is_print);
}
