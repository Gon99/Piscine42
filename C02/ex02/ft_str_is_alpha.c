int	ft_str_is_alpha(char *str)
{
	int		i;
	int		isAlpha;

	i = 0;
	isAlpha = 1;
	while (str[i] != '\0')
	{
		if ((str[i] < 'A' || str[i] > 'Z') && (str[i] < 'a' || str[i] > 'z'))
			isAlpha = 0;
		i++;
	}
	return (isAlpha);
}
